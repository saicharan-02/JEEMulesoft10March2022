

  class PrimitivesDemo {

    public static void main(String[] args) {

     byte v = 30;
     int a = 10;

     float f = 20.3f;
     double d = 10.2d;

     char ch = ' ';

     boolean b = false;

     System.out.println("v = "+v);
     System.out.println("a = "+a);
     System.out.println("f = "+f);
     System.out.println("d = "+d);
     System.out.println("ch = "+ch);
     System.out.println("b = "+b);   
   }

 }